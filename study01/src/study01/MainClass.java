package study01;

public class MainClass {

	public static void main(String[] args) {
		
		byte by;
		short sh;
		int i;
		long l;
		
		by = 4;
		sh = 12;
		i = 123;
		l = 123456L;
		
		System.out.println(by + sh + i + l); // 자동 연산됨
		
		char c;
		String str;
		
		c = 'a';
		str = "hi";
		
		System.out.println(c + str); // 마찬가지 자동 연산
		
	}

}
